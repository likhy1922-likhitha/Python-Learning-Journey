# slicing the Strings
myName = "Likhitha"
print(len(myName))#counts the characters gives the count(length)
s1 = myName[0:6]# printing till the character t 
# frist index is included and last index is not included
print(s1)
s2 = myName[0:3]
print(s2)
s3 = myName[0:5]
print(s3)
# for reverse indexing
print("for reverse indexing")
s4 = myName[-8:-3]
print(s4)
#slicing with skiping values
n = "Likhitha"
