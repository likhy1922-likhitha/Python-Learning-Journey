class Employee:
    name = "Likhitha"
    language = "python"
    salary = 12000000

harry = Employee()
harry.name="harry"
print(harry.name,harry.language,harry.salary)

rohan = Employee()
rohan.name="rohan"
print(rohan.name,rohan.salary,rohan.language)
#here name is a attribute also called as a instance attribute as it is belong to object and salary and language are class attribute as they directly belong  to the class